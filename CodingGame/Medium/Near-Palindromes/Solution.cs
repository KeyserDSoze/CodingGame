    internal static class StringExtensions
    {
        public static bool IsPalindrome(this string value)
        {
            int count = value.Length / 2;
            for (int i = 0; i < count; i++)
                if (value[i] != value[value.Length - 1 - i])
                    return false;
            return true;
        }
        public static bool IsNearPalindrome(this string value)
        {
            if (value.Length > 50)
            {
                Dictionary<char, int> second = value.Substring(value.Length / 2 + value.Length % 2, value.Length / 2).ToCharArray().GroupBy(x => x).ToDictionary(x => x.Key, x => x.Count());
                int difference = 0;
                foreach (var first in value.Substring(0, value.Length / 2).ToCharArray().GroupBy(x => x))
                {
                    if (second.ContainsKey(first.Key))
                        difference += Math.Abs(second[first.Key] - first.Count());
                    else
                        difference += first.Count();
                    if (difference > 1)
                        return false;
                }
                if (difference > 1)
                    return false;
            }
            if (value.IsPalindrome())
                return true;
            for (int i = 0; i < value.Length; i++)
                if ($"{value.Substring(0, i)}{value.Substring(i + 1, value.Length - i - 1)}".IsPalindrome())
                    return true;
            foreach (char attempt in value.ToCharArray().Distinct())
            {
                for (int i = 0; i <= value.Length; i++)
                {
                    if ($"{value.Substring(0, i)}{attempt}{value.Substring(i, value.Length - i)}".IsPalindrome())
                        return true;
                    if ($"{value.Substring(0, i)}{attempt}{(i + 1 < value.Length ? value.Substring(i + 1, value.Length - i - 1) : string.Empty)}".IsPalindrome())
                        return true;
                }
            }
            return false;
        }
