        public void Run()
        {
            const string zero = "0";
            const string one = "1";
            int N = Inputs.Count;
            StringBuilder stringBuilder = new StringBuilder();
            for (int i = 0; i < N; i++)
            {
                string word = Inputs[i];
                stringBuilder.Append(word.IsNearPalindrome() ? one : zero);
            }

            Console.WriteLine(stringBuilder.ToString());
        }
